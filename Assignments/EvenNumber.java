//If and Else Condition
//Question: Write a Java program to check if a given number is even or odd using if-else statements.

class EvenNumber  
{
	public static void main(String[] args) 
	{
		int num =10;
		if(num%2==0)
		System.out.println("It is even number");
		else
		System.out.println("It is not even number");
	}
}
